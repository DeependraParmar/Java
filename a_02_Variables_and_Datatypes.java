public class a_02_Variables_and_Datatypes {
    public static void main(String[] args) {
//        Variables are the container that are memory allocations capable of storing data in the memory
//        Datatype are basically identifiers that are responsible to identify the type of the data that is to be inserted
//        in the variable and it is also responsible for the allocation of memory of the variable
        int a = 8;
        float b = (float) 9.90;
        char c = 'a';

        //The value which is being stored in the variable of any type is called as the Literal...

//        now we are printing the values of the variables above.
        System.out.println("Integer is: "+ a);
        System.out.println("Float is: "+b);
        System.out.println("Character is: " + c);



    }
}
